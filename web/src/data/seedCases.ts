/**
 * 根据案卷 id 查找案卷
 * @param id - 案卷唯一标识
 * @returns 对应的 Case 对象；未找到时返回 undefined
 */
export function findCaseById(id: string): Case | undefined {
  return seedCases.find((c) => c.id === id);
}

/**
 * 获取案卷列表副本
 * @description 返回不可变的案卷数组副本，避免外部修改影响种子数据。
 * @returns Case[] 案卷数组副本
 */
export function getSeedCases(): readonly Case[] {
  return [...seedCases];
}
